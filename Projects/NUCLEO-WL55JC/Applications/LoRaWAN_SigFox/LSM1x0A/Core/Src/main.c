uint32_t active_app;

/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
#ifdef FEATURE_SUPPORT_IAP_BOOTLOADER  
  SCB->VTOR = START_FLASH_ADDRESS;
#endif /* FEATURE_SUPPORT_IAP_BOOTLOADER */

  HAL_Init();

  SystemClock_Config();

  active_app = SystemApp_Init();
  if(active_app == ACTIVE_APP_LORAWAN)
  {
    MX_LoRaWAN_Init();
  }
  else
  {
    MX_Sigfox_Init();
  }

  while (1)
  {
    if(active_app == ACTIVE_APP_LORAWAN)
    {
      MX_LoRaWAN_Process();
    }
    else
    {
      MX_Sigfox_Process();
    }
  }
}

